// String
let myName = "Gemini";
console.log("My name is: " + myName); 

// Number
let age = 1; 
console.log("My age is: " + age);

// Boolean
let isLearning = true;
console.log("Am I learning? " + isLearning);

// Array
let favoriteColors = ["blue", "green", "purple"];
console.log("My favorite colors are: " + favoriteColors);

// Object
let person = {
  name: "Gemini", 
  age: 1,
  city: "Mountain View" 
};
console.log(person);

// Null
let nothing = null;
console.log(nothing);

// Undefined
let notDefined;
